package coreJava;

public class LoopingSample {
	//for loop
	//while
	//do-while
	//for(initialize the value,condition;increment/decrement of value)
	//print 1 to 10 numbers
	public static void main(String[] args) {
		for(int i=1;i<=100;i++) {//i++ current value will be increased by one,means 1 to 2
		System.out.println(i);
		}//for loop end	
		System.out.println("out of for loop");
		}
	

}
